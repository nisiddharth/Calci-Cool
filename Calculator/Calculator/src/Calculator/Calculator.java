    private void calActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calActionPerformed
        // TODO add your handling code here:
        if(show.getText().equals("")){
            fn=0;
        }
        if(txt.getText().equals("")){
            sn=0;
        }
        if(show.getText().equals("") && txt.getText().equals("")){}
        else{
        fn=Double.parseDouble(show.getText());
        sn=Double.parseDouble(txt.getText());
        }
        switch(op){
            case '+':
                txt.setText(""+(fn+sn));
                show.setText("");
                op=0;
                break;
            case '-':
                txt.setText(""+(fn-sn));
                show.setText("");
                op=0;
                break;
            case '*':
                txt.setText(""+(fn*sn));
                show.setText("");
                op=0;
                break;
            case '/':
                if(sn!=0){
                txt.setText(""+(fn/sn));
                show.setText("");
                op=0;
                }
                else{
                    txt.setText("Error! Can't divide by 0");
                    show.setText("Press C to continue!");
                    op=0;
                }
                break;
            case '^':
                double vvv=Math.pow(fn,sn);
                txt.setText(""+vvv);
                show.setText("");
                op=0;
                break;
            case '%':
                txt.setText(""+fn%sn);
                show.setText("");
                break;
        }
        fn=0; sn=0; result=0;
        op=0;
        int dddd=0, eeee=0;
        String checker=txt.getText();
        for(int i=0;i<checker.length();i++){
            if(checker.charAt(i)=='.')
                dddd++;
            if(checker.charAt(i)=='e')
                eeee++;
        }
            decimal=dddd;
            ecount=eeee;
    }//GEN-LAST:event_calActionPerformed

