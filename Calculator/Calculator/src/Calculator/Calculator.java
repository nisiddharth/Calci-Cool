    private void prckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prckActionPerformed
        // TODO add your handling code here:
        double n=Double.parseDouble(txt.getText());
        int m=(int)n;
        if(n!=m){
            txt.setText("Enter Natural Numbers!");
            show.setText("Press C to continue!");
        }
        else{
            int prco=0;
            for(int i=1;i<=n;i++){
                if(n%i==0)
                    prco++;
            }
            if(prco==2){
                txt.setText(m+" is Prime!");
                show.setText("Press C to continue!");
            }
            else{
                txt.setText(m+" is NOT Prime!");
                show.setText("Press C to continue!");
            }
        }
        decimal=0;
    }//GEN-LAST:event_prckActionPerformed

    private void armsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_armsActionPerformed
        // TODO add your handling code here:
        double n=Double.parseDouble(txt.getText());
        int m=(int)n;
        if(n!=m){
            txt.setText("Enter Natural Numbers!");
            show.setText("Press C to continue!");
        }
        else{
            int dig=0, ddd=m, sum=0;
            while(ddd!=0)
            {
                ddd/=10;
                dig++;
            }
            ddd=m;
            while(ddd!=0)
            {
                sum+=Math.pow((ddd%10),dig);
                ddd/=10;
            }
            if(sum==m)
            {
                txt.setText(m+" is Armstrong");
                show.setText("Press C to continue");
            }
            else{
                txt.setText(m+" is NOT Armstrong");
                show.setText("Press C to continue");
            }
        }
        decimal=0;
    }//GEN-LAST:event_armsActionPerformed

